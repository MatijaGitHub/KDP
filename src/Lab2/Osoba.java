package Lab2;

public class Osoba {
    private String nconst;
    private String name;
    private String birthYear;
    private String deathYear;
    private String[] proffesions;
    private String[] knownTitles;

    public String getNconst() {
        return nconst;
    }

    public void setNconst(String nconst) {
        this.nconst = nconst;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getBirthYear() {
        return birthYear;
    }

    public void setBirthYear(String birthYear) {
        this.birthYear = birthYear;
    }

    public String getDeathYear() {
        return deathYear;
    }

    public void setDeathYear(String deathYear) {
        this.deathYear = deathYear;
    }

    public String[] getProffesions() {
        return proffesions;
    }

    public void setProffesions(String[] proffesions) {
        this.proffesions = proffesions;
    }

    public String[] getKnownTitles() {
        return knownTitles;
    }

    public void setKnownTitles(String[] knownTitles) {
        this.knownTitles = knownTitles;
    }
}
